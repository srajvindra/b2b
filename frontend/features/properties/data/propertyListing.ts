const PROPERTY_GROUPS = ["Group A - Premium", "Group B - Standard", "Group C - Economy", "Group D - Mixed"]

function buildProperty(
  id: number,
  name: string,
  address: string,
  origType: string,
  units: number,
  hasVacancy: boolean,
  ownerName: string,
  dateAdded: string,
  staffName: string,
  portfolioGroup: string,
  tags: string[],
  assigneeRole: string,
): Property {
  const n = id

  return {
    id: String(id),
    name,
    address,
    unitAddress: units === 1 ? address : `Unit ${100 + (n % units)}, ${address}`,
    type: origType === "Single-Family" ? "Single" : ((n % 3 === 0 ? "Apartment" : "Multi") as Property["type"]),
    units,
    hasVacancy,
    ownerName,
    tenantName: hasVacancy ? "-" : TENANTS[n % TENANTS.length],
    occupancyStatus: hasVacancy ? "Vacant" : "Occupied",
    csr: STAFF_CSR[n % STAFF_CSR.length],
    csm: STAFF_CSM[n % STAFF_CSM.length],
    agm: STAFF_AGM[n % STAFF_AGM.length],
    lc: STAFF_LC[n % STAFF_LC.length],
    fc: STAFF_FC[n % STAFF_FC.length],
    mrs: STAFF_MRS[n % STAFF_MRS.length],
    dateAdded,
    staffName,
    portfolioGroup,
    propertyGroup: PROPERTY_GROUPS[n % PROPERTY_GROUPS.length],
    tags,
    assigneeRole,
    propertyStatus: (
      n % 7 === 0
        ? "Under Termination"
        : n % 11 === 0
          ? "Hidden"
          : n % 13 === 0
            ? "Under Retention"
            : "Active"
    ) as NonNullable<Property["propertyStatus"]>,
  }
}

